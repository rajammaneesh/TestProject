Create table log(
Id int NOT NULL auto_increment,
description varchar(255),
details varchar(255),
user varchar(50),
primary key (ID)
) auto_increment=1;

Create table suggestion(
Id int NOT NULL auto_increment,
description varchar(255),
details varchar(255),
user varchar(50),
primary key (ID)
) auto_increment=1;

CREATE TABLE SERVICE_LINE (
  ID int(11) NOT NULL AUTO_INCREMENT,
  Name varchar(20) NOT NULL,
  Description varchar(50) NOT NULL,
  PRIMARY KEY (ID),
  UNIQUE KEY (Name),
  UNIQUE KEY (Description)
) auto_increment = 1;

Create TABLE USERS(
	ID INT NOT NULL auto_increment,
	FIRST_NAME varchar(50) NULL,
	LAST_NAME varchar(50) NULL,
	DESIGNATION varchar(50) NULL,
	EMAIL_ID varchar(255) NOT NULL,
	PROJECT_NAME varchar(255) NULL,
	PROJECT_CODE varchar(255) NULL,
	PROJECT_MANAGER_NAME varchar(255) NULL, 
	MANAGER_EMAIL_ID varchar(255) NULL,
	STATUS varchar(20) NULL,
	STATUS_DATE datetime NULL,
	CREATED_BY varchar(100) NULL,
	CREATED_ON datetime NULL,
	UPDATED_BY varchar(100) NULL,
	UPDATED_ON datetime NULL,
PRIMARY KEY(ID)
) auto_increment=1;

CREATE TABLE TASKS (
  ID int(11) NOT NULL AUTO_INCREMENT,
  USER_ID int(11) NOT NULL,
  PROJECT_NAME varchar(50) NOT NULL,
  TASK_NAME varchar(50) DEFAULT NULL,
  PROJECT_WBS_Code varchar(50) NOT NULL,
  TYPE varchar(100) DEFAULT NULL,
  TASK_TYPE_ID int(11) DEFAULT NULL,
  DETAILS varchar(255) DEFAULT NULL,
  HOURS int(11) DEFAULT NULL,
  COMMENTS varchar(255) DEFAULT NULL,
  GIFTS bit(1) DEFAULT NULL,
  ONBOARDING_DATE datetime DEFAULT NULL,
  SERVICE_LINE_ID int(11) NOT NULL,
  DUE_DATE datetime DEFAULT NULL,
  STATUS varchar(20) DEFAULT NULL,
  STATUS_DATE datetime DEFAULT NULL,
  CREATED_BY varchar(100) DEFAULT NULL,
  CREATED_ON datetime DEFAULT NULL,
  UPDATED_BY varchar(100) DEFAULT NULL,
  UPDATED_ON datetime DEFAULT NULL,
  PRIMARY KEY (ID),
  KEY `USER_ID` (USER_ID),
  KEY `SERVICE_LINE_ID` (SERVICE_LINE_ID),
  KEY `TASK_TYPE_ID` (TASK_TYPE_ID),
  CONSTRAINT tasks_ibfk_3 FOREIGN KEY (TASK_TYPE_ID) REFERENCES task_type (ID) ON DELETE NO ACTION ON UPDATE NO ACTION,
  CONSTRAINT tasks_ibfk_1 FOREIGN KEY (USER_ID) REFERENCES users (ID),
  CONSTRAINT tasks_ibfk_2 FOREIGN KEY (SERVICE_LINE_ID) REFERENCES service_line (ID)
   )auto_increment=1;

CREATE TABLE TASKAPPLICANTS(
	ID INT NOT NULL auto_increment,
	TASK_ID INT NOT NULL,
	APPLICANT_ID INT NOT NULL,
	STATEMENT_OF_PURPOSE varchar(500) DEFAULT NULL,
	STATUS varchar(20) NULL,
	STATUS_DATE datetime NULL,
	CREATED_BY varchar(100) NULL,
	CREATED_ON datetime NULL,
	UPDATED_BY varchar(100) NULL,
	UPDATED_ON datetime NULL,
PRIMARY KEY(ID),
FOREIGN KEY(APPLICANT_ID) references USERS(ID),
FOREIGN KEY(TASK_ID) references TASKS(ID)
)auto_increment=1;

CREATE TABLE APPROVEDAPPLICANT(
	ID INT NOT NULL auto_increment,
	APPLICANT_ID INT NOT NULL,
	TASK_ID	int NOT NULL,
	HOURS_WORKED decimal(5, 1) NULL,
	RATING varchar(50) NULL,
	WORK_AGAIN bit NULL,
	POINTS int NULL,
	COMMENTS varchar(255) NULL,
	STATUS varchar(20) NULL,
	STATUS_DATE datetime NULL,
	CREATED_BY varchar(100) NULL,
	CREATED_ON datetime NULL,
	UPDATED_BY varchar(100) NULL,
	UPDATED_ON datetime NULL,
PRIMARY KEY(ID),
FOREIGN KEY(APPLICANT_ID) references USERS(ID),
FOREIGN KEY(TASK_ID) references TASKS(ID)
)auto_increment=1;


CREATE TABLE SKILLS(
	ID INT NOT NULL auto_increment,
	VALUE varchar(200) NOT NULL,
	STATUS varchar(20) NULL,
	STATUS_DATE datetime NULL,
	CREATED_BY varchar(100) NULL,
	CREATED_ON datetime NULL,
	UPDATED_BY varchar(100) NULL,
	UPDATED_ON datetime NULL,
PRIMARY KEY(ID)
)auto_increment=1;

CREATE TABLE APPLICANTSKILLS(
	ID INT NOT NULL auto_increment,
	APPLICANT_ID INT NOT NULL,
	SKILL_ID INT NOT NULL,
	STATUS varchar(20) NULL,
	STATUS_DATE datetime NULL,
	CREATED_BY varchar(100) NULL,
	CREATED_ON datetime NULL,
	UPDATED_BY varchar(100) NULL,
	UPDATED_ON datetime NULL,
PRIMARY KEY(ID),
FOREIGN KEY(APPLICANT_ID) references USERS(ID),
FOREIGN KEY(SKILL_ID) references SKILLS(ID)
)auto_increment=1;


CREATE TABLE TASKSKILLS(
	ID INT NOT NULL auto_increment,
	TASK_ID INT NOT NULL,
	SKILL_ID INT NOT NULL,
	STATUS varchar(20) NULL,
	STATUS_DATE datetime NULL,
	CREATED_BY varchar(100) NULL,
	CREATED_ON datetime NULL,
	UPDATED_BY varchar(100) NULL,
	UPDATED_ON datetime NULL,
PRIMARY KEY(ID),
FOREIGN KEY(TASK_ID) REFERENCES TASKS(ID),
FOREIGN KEY(SKILL_ID) references SKILLS(ID)
)auto_increment = 1;


